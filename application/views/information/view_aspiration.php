		<h2><span class="label label-default ">ปณิธาน / วัตถุประสงค์</span></h2>
		<div class="clearfix visible-xs"></div>
        <br>

        <blockquote>
        <h3><span class="label label-default ">ปณิธานร่วม</span></h3>
        <div class="clearfix visible-xs"></div>
        <p style="margin: 0px 100px 10px;text-align:justify">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"ปัญญาเพื่อความเข้มแข็งของชุมชน” หรือ “Wisdom for Community Empowerment"</p>
        </blockquote>
        <hr>


        <blockquote>
        <h3><span class="label label-default ">ปรัชญา</span></h3>
        <div class="clearfix visible-xs"></div>
        <p style="margin: 0px 100px 10px;text-align:justify">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"ปญฺญาชีวิ เสฏฺฐชีวี นาม (ชีวิตที่เป็นอยู่ด้วยปัญญา จัดเป็นชีวิตประเสริฐที่สุด)"</p>
        </blockquote>
        <hr>

        <blockquote>
        <h3><span class="label label-default ">วิสัยทัศน์</span></h3>
        <div class="clearfix visible-xs"></div>
        <p style="margin: 0px 100px 10px;text-align:justify">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"เทคโนโลยีสารสนเทศและการสื่อสาร สรรค์สร้างองค์ความรู้ สร้างสานชีวิตสู่สากล"</p>
        </blockquote>
        <hr>

        <blockquote>
        <h3><span class="label label-default ">ค่านิยมร่วม</span></h3>
        <div class="clearfix visible-xs"></div>
        <p style="margin: 0px 100px 10px;text-align:none">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp1. Competence - หลักความรู้ความสามารถ (ให้ความสำคัญกับผู้ที่มีความสามารถสูงเป็นอันดับแรกก่อนเสมอ) (จึงเป็นหน้าที่ของทุกคนในองค์กรที่จะต้องช่วยกันยกระดับความรู้ความสามารถของตนเองให้ดียิ่งขึ้นตลอดเวลา) (เพื่อส่งเสริม สนับสนุนให้เกิด Competitiveness และ Leadership)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp2. Freedom - หลักเสรีภาพ (มีเสรีภาพที่จะคิดที่จะทำงานให้บรรลุวัตถุประสงค์) (เพื่อส่งเสริมสนับ สนุนให้เกิด Excellence) (ทำให้เกิดความเป็นเลิศที่หลากหลายตามที่ตนเองถนัดหรือเชี่ยวชาญ)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp3. Justice – หลักความถูกต้องยุติธรรม (มีความถูกต้องและยุติธรรมอยู่ในหัวใจตลอดเวลา) (มีคุณธรรมและจรรยาบรรณ) (ทำให้เกิดความเคารพนับถือและความไว้เนื้อเชื่อใจซึ่งกันและกัน)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp4. Generosity - หลักความมีน้ำใจ (มีน้ำใจช่วยเหลือเกื้อกูลกัน โดยให้ถือเป็น"สิทธิ" ของผู้ที่อ่อนแอกว่าที่จะได้รับความช่วยเหลือ และเป็น "หน้าที่" ของผู้ที่เข้มแข็งกว่าที่จะต้องให้การช่วยเหลือ) (ทำให้เกิดวัฒนธรรมการอยู่ร่วมกันอย่างมีความสุข)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5. Team Learning and Working - หลักการแลกเปลี่ยนเรียนรู้และทำงานเป็นทีม (เน้นการเรียนรู้จากการทำงานและการดำเนินชีวิต) (ทำให้เกิดปัญญารวมหมู่)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp6. Shared Vision - หลักการมีเป้าหมายร่วมกัน (มีเป้าหมายในการทำงานร่วมกัน) (ทำให้เกิดพลังสามัคคี) (ทำให้เกิดเป็นองค์กรแห่งการเรียนรู้)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp7. Local and Global Spirit - หลักความเชื่อมโยงระหว่างชุมชนและสากล (ยึดคติ "องค์ความรู้ที่ผลิตและที่ใช้เป็นระดับสากล ส่วนจิตวิญญาณอยู่ที่การรับใช้ชุมชนและสังคมไทย") (ทำให้เกิดความเป็นสากล หรือ Internationalization)</p>
        </blockquote>
        <hr>

        <blockquote>
        <h3><span class="label label-default ">วัตถุประสงค์ร่วม</span></h3>
        <div class="clearfix visible-xs"></div>
        <p style="margin: 0px 100px 10px;text-align:none">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp1. เพื่อจัดการเรียนการสอนที่เน้นให้นิสิตอยู่และเรียน (Live and Learn) อย่างมีความสุข จบไปมีงานทำและเป็นคนดีของสังคม<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp2. เพื่อทำการวิจัยที่เน้นการสร้างปัญญารวมหมู่ (Collective Intelligence) เคียงคู่ชุมชน (สนับสนุนแนวคิดOUOP – One University One Province)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp3. เพื่อบริการวิชาการโดยเน้นการใช้ปัญญารวมหมู่เพื่อพัฒนาความเข้มแข็งของชุมชน(Community Empowerment)<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp4. เพื่อทำนุบำรุงภูมิปัญญา ศิลปะ วัฒนธรรม และสิ่งแวดล้อมของท้องถิ่น (Local Wisdom) สู่สากล<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp5. เพื่อบริหารจัดการอย่างมีประสิทธิภาพ ประสิทธิผล และยึดมั่นในธรรมาภิบาล</p>
        </blockquote>
